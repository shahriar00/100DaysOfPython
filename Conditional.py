marks = int(input("Enter your course marks:"))
if marks>=80:
    print("Congratulations!You got A+")
elif marks>=70:
    print("You got A")
elif marks>=60:
    print("You got A-")
elif marks>=50:
    print("You got B")
elif marks>=40:
    print("You got C")
elif marks>=33:
    print("You got D")
else:
    print("Sorry! You are Failed")


